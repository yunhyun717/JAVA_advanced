package kopo38_swc06;

public class kopo38_P06_1 {

	public int kopo38_sum(int a, int b) {
		return a + b;
	}
	// 메소드 kopo38_sum을 두 정수 인자의 합을 return값으로 갖도록 설정한다.
	
	public int kopo38_sum(int a, int b, int c) {
		return a + b + c;
	}
	// 메소드 kopo38_sum을 세 정수 인자의 합을 return값으로 갖도록 설정한다.
	
	public int kopo38_sum(int a, int b, int c, int d) {
		return a + b + c + d;
	}
	// 메소드 kopo38_sum을 네 정수 인자의 합을 return값으로 갖도록 설정한다.
	
	public double kopo38_sum(double a, double b) {
		return a + b;
	}
	// 메소드 kopo38_sum을 두 double 인자의 합을 return값으로 갖도록 설정한다.
	
	// 각각의 메소드는 모두 같은 이름을 갖고 있지만 인자의 종류와 숫자에 따라 다른 내용을 갖는다.
	// 이를 메소드 오버로딩이라고 한다.
}
