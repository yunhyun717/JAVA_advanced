package kopo38_swc06;

public class kopo38_P07_1 {

	public int kopo38_sum(int a, int b, int c) {
		return a + b + c;
	}
	// kopo38_sum이 세개의 정수 인자의 합을 return 값으로 갖도록 설정한다.

	public int kopo38_avg(int a, int b, int c) {
		return (a + b + c) / 3;
	}
	// kopo38_avg가 세개의 정수 인자의 평균을 return 값으로 갖도록 설정한다.
	
	public int kopo38_sum(int a, int b, int c, int d) {
		return a + b + c + d;
	}
	// kopo38_sum이 네개의 정수 인자의 합을 return 값으로 갖도록 설정한다.
	
	public int kopo38_avg(int a, int b, int c, int d) {
		return (a + b + c + d) / 4;
	}
	// kopo38_avg가 네개의 정수 인자의 평균을 return 값으로 갖도록 설정한다.
	
	public int kopo38_sum(int a, int b, int c, int d, int e) {
		return a + b + c + d + e;
	}
	// kopo38_sum이 다섯개의 정수 인자의 합을 return 값으로 갖도록 설정한다.
	
	public double kopo38_avg(int a, int b, int c, int d, int e) {
		return (a + b + c + d + e) / 5;
	}
	// kopo38_avg가 다섯개의 정수 인자의 평균을 return 값으로 갖도록 설정한다.
	
}
